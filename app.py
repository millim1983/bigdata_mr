# 스트림릿 라이브러리 사용하기 위한 임포트
import streamlit as st

# 웹 대시보드 개발 라이브러르 스트림릿은 main 함수가 있어야 한다 

def main():
    
    st.title('파이썬전문가가 되자')
    st.header('데이타 전문가가 되자')
    st.subheader('어여 나으세요')
    st.success('겸둥이 동영이를 사랑해')
    st.warning('술 마시면 경고')
    st.info('오늘은 늦는다')
    st.error('비가많이온다')
    st.error('재미따 껄껄')
    st.title('연결된건가')
    

    

if __name__ == '__main__':
    main()


